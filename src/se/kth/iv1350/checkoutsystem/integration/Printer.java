package se.kth.iv1350.checkoutsystem.integration;

/**
 * Class representing the printer
 */
public class Printer {
    /**
     * Constructor for the printer
     */
    public Printer() {
    }

    /**
     * Method for printing string-object to the console.
     * @param stringToBePrinted The string object which will be printed.
     */
    public void print(String stringToBePrinted){
        System.out.println(stringToBePrinted);
    }
}
